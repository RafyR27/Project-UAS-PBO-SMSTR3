    public void showPayment(int id_user){
        this.id_user = id_user;
        
        userServices.getAlamatUser(id_user);
        
        ArrayList<ModelTransaction> products = transactionServices.getTransaction(id_user);
        
        total = 0;
        
        containerProduct.removeAll();
        
        for (ModelTransaction item : products) {
            PaymentList p = new PaymentList(); 
            p.setData(item);
            containerProduct.add(p);
            total += item.getTotal_harga();
        }
        
        lbTotalSemua.setText("Rp. " + nf.format(total));
        lbTotalSemua2.setText("Rp. " + nf.format(total));
        lbNama.setText(user.getFullname());
        lbAlamat.setText(ma.getAlamat() + ", " + ma.getKode_pos());
        
        containerProduct.revalidate();
        containerProduct.repaint();
    }

