    private void ButtonBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBuyActionPerformed
        if(metode_pembayaran.length() == 0){
            JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), "Pilih salah satu metode pembayaran");
        } else {
            orderId = "ORD" + System.currentTimeMillis();
            int result = transactionServices.updateOrderId(orderId, id_user);
            if(result == 200){
                int paymentResult = transactionServices.paymentService(orderId, metode_pembayaran, total);
                if(paymentResult == 200){
                    MainFrame main = (MainFrame) SwingUtilities.getWindowAncestor(this);
                    main.showCheckPaymentPage(metode_pembayaran, orderId, total);
                } else {
                    JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), "Internal Server Error");
                }
            } else {
                JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), "Internal Server Error");
            }
        }
    }//GEN-LAST:event_ButtonBuyActionPerformed

    private void ButtonUbahAlamatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonUbahAlamatMouseClicked
        JTextField alamat = new JTextField(ma.getAlamat());
        JTextField no_telp = new JTextField(ma.getNo_telp());
        JTextField kode_pos = new JTextField(ma.getKode_pos());

        Object[] alamatUser = {
            "Alamat:", alamat,
            "Nomor Telphone:", no_telp,
            "Kode Pos:", kode_pos,
        };

        int result = JOptionPane.showConfirmDialog(SwingUtilities.getWindowAncestor(this), alamatUser, "Lengkapi Alamat Anda", JOptionPane.OK_CANCEL_OPTION);
        if(result == JOptionPane.OK_OPTION){
            if(alamat.getText().trim().isEmpty() || no_telp.getText().trim().isEmpty() || kode_pos.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), "Data tidak boleh kosong");
                return;
            }

           int resultUpdateAlamat = userServices.updateAlamatUser(id_user, alamat.getText().trim(), no_telp.getText().trim(), kode_pos.getText().trim());
           if(resultUpdateAlamat == 200){
                int okClick = JOptionPane.showConfirmDialog(SwingUtilities.getWindowAncestor(this),"Alamat berhasil diubah","Informasi",JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
                if (okClick == JOptionPane.OK_OPTION) {
                    MainFrame main = (MainFrame) SwingUtilities.getWindowAncestor(this);
                    main.showPaymentPage(id_user);
                }
            } else if(resultUpdateAlamat == 404){
                JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), "Data tidak ditemukan");
            } else {
                JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), "Internal Server Error");
            }
        }
    }//GEN-LAST:event_ButtonUbahAlamatMouseClicked


